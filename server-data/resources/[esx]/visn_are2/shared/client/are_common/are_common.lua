function _sendNotification(notificationMessage, notificationType, notificationTimeout)
    notificationTimeout = notificationTimeout or 5000

    --[[
    TriggerEvent('dreamwork_notify:SendNotification', {
        layout = "bottomLeft",
        type = notificationType,
        timeout = notificationTimeout,
        theme = "gta",
        animation = {
            open = "gta_effects_open_left",
            close = "gta_effects_close_left"
        },
        text = notificationMessage
    })]]

    ESX.ShowNotification(notificationMessage)
end

function has_value (tab, val)
    for index, value in ipairs(tab) do
        if value == val then
            return true
        end
    end

    return false
end

function pairsByKeys(t, f)
    local a = {}
    for n in pairs(t) do
        table.insert(a, n)
    end
    table.sort(a, f)
    local i = 0      -- iterator variable
    local iter = function ()   -- iterator function
        i = i + 1
        if a[i] == nil then
            return nil
        else
            return a[i], t[a[i]]
        end
    end
    return iter
end
